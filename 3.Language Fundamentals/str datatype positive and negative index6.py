#Data Types: str data type - positive and negative index

#In python we have the index which is to access the charcter of the string or any other datatype like list tuple we did not cover list tuple in this.

s = 'Lokesh'

#Now to access the e from lokesh string we use index and index starts with the 0 1 2 3 4 5 like this is flows


#in the above mentioned name we positions like this L->0, o->1, k->2, e->3, s->4, h->5

print(s[0]) #prints L because L is in 0 index position
print(s[3]) #prints e because e is in 3 index position


#NOW NEGATIVE INDEX

#In negative index positions we have number starts from -1, -2, -3, -4, -5 like this it flows


s= 'Lokesh' # -1 -> h, s -> -2, -3 -> e, -4 -> k like this it flows
